def get_random_emoji(category="general"):
    if category == "settings":
        emoji_id = random.choice(SETTINGS_EMOJIS)
        return f'<emoji id="{emoji_id}">✨</emoji>'
    elif category == "searching":
        emoji_id1 = random.choice(SEARCHING_EMOJIS)
        emoji_id2 = random.choice(SEARCHING_EMOJIS)
        return f'<emoji id="{emoji_id1}">🍭</emoji> <emoji id="{emoji_id2}">🍭</emoji>'
    else:
        emoji_id = random.choice(PREMIUM_EMOJIS)
        return f'<emoji id="{emoji_id}">✨</emoji>'

class LangDict(dict):
    def __getitem__(self, key):
        value = super().__getitem__(key)
        if isinstance(value, str):
            if "start" in key or "settings" in key:
                category = "settings"
            elif "searching" in key:
                category = "searching"
            else:
                category = "general"
            
            if "{emoji}" in value:
                while "{emoji}" in value:
                    value = value.replace("{emoji}", get_random_emoji(category), 1)
            elif len(value) > 20 or "\n" in value:
                value += f" {get_random_emoji(category)}"
        return value

